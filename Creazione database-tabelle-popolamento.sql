CREATE DATABASE VendiCoseDB;

USE VendiCoseDB;

CREATE TABLE Prodotti (
	IDProdotto INT NOT NULL AUTO_INCREMENT PRIMARY KEY
    ,NomeProdotto VARCHAR(100) NOT NULL
    ,Prezzo DECIMAL(18,2) NOT NULL
    ,IDCategoria INT NOT NULL 
,FOREIGN KEY (IDCategoria) REFERENCES categoria(IDCategoria)
);

CREATE TABLE Categoria (
IDCategoria INT NOT NULL AUTO_INCREMENT PRIMARY KEY
,NomeCategoria VARCHAR(100)
,DescrizioneCategoria VARCHAR(150)
);

CREATE TABLE Vendite (
	IDVendita INT NOT NULL AUTO_INCREMENT PRIMARY KEY
    ,DataVendita DATE NOT NULL
    ,IDNegozio INT NOT NULL
,FOREIGN KEY (IDNegozio) REFERENCES Negozio(IDNegozio)
);

CREATE TABLE Negozio (
	IDNegozio INT NOT NULL AUTO_INCREMENT PRIMARY KEY
    ,NomeNegozio VARCHAR(100) NOT NULL
    ,Indirizzo VARCHAR(100) NOT NULL
    ,Citta VARCHAR(50) NOT NULL
    ,IDMagazzino INT NOT NULL
    ,FOREIGN KEY (IDMagazzino) REFERENCES Magazzino(IDMagazzino)
);

CREATE TABLE MovimentiMagazzino (
	IDMovimentoMagazzino INT NOT NULL AUTO_INCREMENT PRIMARY KEY
    ,QuantitaGiacenza INT NOT NULL
    ,IDProdotto INT NOT NULL
    ,IDMagazzino INT NOT NULL
,FOREIGN KEY (IDProdotto) REFERENCES prodotti(IDProdotto)
,FOREIGN KEY (IDMagazzino) REFERENCES magazzino(IDMagazzino)
);
    
CREATE TABLE Magazzino (
	IDMagazzino INT NOT NULL AUTO_INCREMENT PRIMARY KEY
    ,NomeMagazzino VARCHAR(100) NOT NULL
    ,Indirizzo VARCHAR(100)
);

CREATE TABLE OrdineRestock (
	IDOrdineRestock INT NOT NULL AUTO_INCREMENT PRIMARY KEY
    ,Quantita INT NOT NULL
    ,DataOrdine DATE NOT NULL
    ,StatoOrdine VARCHAR(100) NOT NULL
    ,SogliaMinima INT NOT NULL
    ,IDMagazzino INT NOT NULL
    ,IDCategoria INT NOT NULL
,FOREIGN KEY (IDMagazzino) REFERENCES magazzino(IDMagazzino)
,FOREIGN KEY (IDCategoria) REFERENCES categoria(IDCategoria)
);

CREATE TABLE DettaglioVendite (
	IDDettaglioVendita INT NOT NULL AUTO_INCREMENT PRIMARY KEY
    ,Prezzo DECIMAL(18,2) NOT NULL
    ,Quantità INT NOT NULL
    ,IDProdotto INT NOT NULL
    ,IDVendita INT NOT NULL
,FOREIGN KEY (IDProdotto) REFERENCES prodotti(IDProdotto)
,FOREIGN KEY (IDVendita) REFERENCES vendite(IDVendita)
);

